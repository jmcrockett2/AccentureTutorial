import static java.lang.System.out;

public class Application
{
    public static void main(String[] args)
    {
        int myNumber = 88;
        short myShort = 847;
        long myLong = 9797;

        double myDouble = 7.3243;
        float myFloat = 324.3f;

        char myChar = 'y';
        boolean myBoolean = false;

        byte myByte = 127;

        out.println(myNumber);
        out.println(myShort);
        out.println(myLong);
        out.println(myDouble);
        out.println(myFloat);
        out.println(myChar);
        out.println(myBoolean);
        out.println(myByte);
    }
}
